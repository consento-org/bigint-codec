const { test } = require('fresh-tape')
const { Buffer } = require('buffer')
const bigIntBE = require('../bigint-be')
const { bytesStr } = require('./lib')

test(bigIntBE.name, t => {
  t.deepEquals(bigIntBE.decode([]), 0n)
  for (let [num, bytes] of [
    [0, [0]],
    [1, [1]],
    [127, [127]],
    [128, [0, 128]],
    [129, [0, 129]],
    [255, [0, 255]],
    [256, [1, 0]],
    [257, [1, 1]],
    [382, [1, 126]],
    [383, [1, 127]],
    [384, [1, 128]],
    [512, [2, 0]],
    [513, [2, 1]],
    [65535, [0, 255, 255]],
    [65536, [1, 0, 0]],
    [65537, [1, 0, 1]],
    [65538, [1, 0, 2]],
    [16777215, [0, 255, 255, 255]],
    [
      74156619719791337109230812903812383810298301928301238383103810923n,
      [0x00, 0xb4, 0x43, 0xc7, 0x1a, 0x6e, 0xc1, 0x4a, 0xb7, 0x55, 0x15, 0x41, 0xe9, 0xf2, 0x6a, 0xf6, 0x33, 0x7f, 0x3f, 0xde, 0xa4, 0x69, 0x3d, 0x8d, 0x68, 0x3d, 0x9d, 0x6b]
    ],
    [-1, [255]],
    [-127, [129]],
    [-128, [128]],
    [-129, [255, 127]],
    [-255, [255, 1]],
    [-256, [255, 0]],
    [-257, [254, 255]],
    [-382, [254, 130]],
    [-383, [254, 129]],
    [-384, [254, 128]],
    [-512, [254, 0]],
    [-513, [253, 255]],
    [-65535, [255, 0, 1]],
    [-65536, [255, 0, 0]],
    [-65537, [254, 255, 255]],
    [-65538, [254, 255, 254]],
    [-16777215, [255, 0, 0, 1]],
    [
      -74156619719791337109230812903812383810298301928301238383103810923n,
      [0xff, 0x4b, 0xbc, 0x38, 0xe5, 0x91, 0x3e, 0xb5, 0x48, 0xaa, 0xea, 0xbe, 0x16, 0x0d, 0x95, 0x09, 0xcc, 0x80, 0xc0, 0x21, 0x5b, 0x96, 0xc2, 0x72, 0x97, 0xc2, 0x62, 0x95]
    ]
  ]) {
    num = BigInt(num)
    t.deepEquals(bigIntBE.encode(num), Buffer.from(bytes), `encode(${num.toString()}n)`)
    t.equals(bigIntBE.decode(Buffer.from(bytes)), num, `decode(${bytesStr(bytes)})`)
  }
  t.end()
})
